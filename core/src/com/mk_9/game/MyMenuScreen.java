package com.mk_9.game;

import com.badlogic.gdx.Gdx;
import com.badlogic.gdx.Screen;
import com.badlogic.gdx.graphics.GL20;
import com.badlogic.gdx.graphics.OrthographicCamera;

/**
 * Created by mk 9 on 15.05.2016.
 */
public class MyMenuScreen implements Screen {
    private Rain game;
    OrthographicCamera camera;

    public MyMenuScreen(Rain game) {
        this.game = game;

        camera = new OrthographicCamera();
        camera.setToOrtho(false, Rain.WIDTH, Rain.HEIGHT);
    }


    @Override
    public void show() {

    }

    @Override
    public void render(float delta) {
        Gdx.gl.glClearColor(1, 0, 0.2f, 1);
        Gdx.gl.glClear(GL20.GL_COLOR_BUFFER_BIT);

        camera.update();
        game.batch.setProjectionMatrix(camera.combined);

        game.batch.begin();
        game.font.draw(game.batch, "Explosive Android Rain ", 100, 150);
        game.font.draw(game.batch, "LETS ROCK, BABY", 100, 100);
        game.batch.end();

        if (Gdx.input.isTouched()) {
            game.setScreen(new GameScreen(game));
            dispose();
        }

    }

    @Override
    public void resize(int width, int height) {

    }

    @Override
    public void pause() {

    }

    @Override
    public void resume() {

    }

    @Override
    public void hide() {

    }

    @Override
    public void dispose() {

    }
}
